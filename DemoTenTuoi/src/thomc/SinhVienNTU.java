package thomc;

public class SinhVienNTU {
	String mssv;
	String hoVaTen;
	public SinhVienNTU(String mssv, String hoVaTen) {
		super();
		this.mssv = mssv;
		this.hoVaTen = hoVaTen;
	}
	public SinhVienNTU() {
		super();
	}
	public String getMssv() {
		return mssv;
	}
	public void setMssv(String mssv) {
		this.mssv = mssv;
	}
	public String getHoVaTen() {
		return hoVaTen;
	}
	public void setHoVaTen(String hoVaTen) {
		this.hoVaTen = hoVaTen;
	}
	
	
}
